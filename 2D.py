#二維列表、巢狀迴圈
#二維列表[行][列]

nums = [
 #↓列0,1,2   #←行
    [0,1,2], # 0
    [3,5,7], # 1
    [2,4,6]  # 2
    ]

print(nums[1][2]) #第1行第2列，輸出結果為7

#如果要印出二維列表的話需要使用「巢狀迴圈」
#巢狀迴圈就是迴圈中又包含迴圈
#row = 行
#column = 列

for row in nums: #先將nums逐行存入row → [0,1,2],[3,5,7],[2,4,6]
    for col in row: #再將row逐個存入col → [0,1,2,3,5,7,2,4,6]
        print(col)